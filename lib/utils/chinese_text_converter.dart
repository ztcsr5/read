  static final Map<String, String> _simplifiedToTraditional =
      _traditionalToSimplified.map((key, value) => MapEntry(value, key));

  static String convert(String text, int mode) {
    if (mode == simplified) return _convert(text, _traditionalToSimplified);
    if (mode == traditional) return _convert(text, _simplifiedToTraditional);
    return text;
  }

  static String _convert(String text, Map<String, String> mapping) {
    if (text.isEmpty) return text;
    final buffer = StringBuffer();
    for (final rune in text.runes) {
      final char = String.fromCharCode(rune);
      buffer.write(mapping[char] ?? char);
    }
    return buffer.toString();
  }
